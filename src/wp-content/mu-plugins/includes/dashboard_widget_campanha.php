<p>Você está no Painel de controle da <i>Rede Livre</i>, agora você pode gerenciar todas as ferramentas utilizando o menu da sua lateral esquerda, mas sempre lembre que quando estiver logado haverá uma barra superior com opções de gerenciamento rápido.</p>
<p>Em todas as etapas não esqueça de salvar sempre suas opções.</p>

<p>Você pode começar pela aparência do site:</p>
<ol>
    <li><a href="<?php echo admin_url('themes.php'); ?>">Escolha o seu tema.</a></li>
    <li><a href="<?php echo admin_url('widgets.php'); ?>">Defina o conteúdo e funcionalidades da coluna lateral do seu site</a>.</li>
    <li><a href="<?php echo admin_url('nav-menus.php'); ?>">Organize o menu com as informações da sua campanha</a>.</li>
    <li><a href="<?php echo admin_url('themes.php?page=theme_options'); ?>">Edite as opções do seu tema</a>.</li>
    <li><a href="<?php echo admin_url('themes.php?page=custom-header'); ?>">Envie uma imagem para o cabeçalho do site com sua foto, nome e número</a>.</li>
    <li><a href="<?php echo admin_url('themes.php?page=custom-background'); ?>">Se desejar, deixe o fundo do seu site com a sua cara</a>.</li>
</ol>

<p>Em seguida no menu <i>Páginas</i> você pode:</p>
<ol>
    <li><a href="<?php echo admin_url('edit.php?post_type=page'); ?>">Editar as páginas existentes</a>.</li>
    <li><a href="<?php echo admin_url('post-new.php?post_type=page'); ?>">Criar novas páginas.</a></li>
    <li>Excluir páginas que você não vai usar.</li>
</ol>

<p>Em <a href="<?php echo admin_url('upload.php'); ?>">Mídia</a> você pode carregar uma foto ou vária fotos que você utilizará durante a campanha.</p>

<p>Nos <a href="<?php echo admin_url('link-manager.php'); ?>">Links</a> você pode colocar os sites que você tem como referência e quer divulgar no seu site.</p>

<p>Em <a href="<?php echo admin_url('edit-comments.php'); ?>">Comentários</a> você poderá gerenciar todos os comentários que fizerem no seu site, aprovando ou excluíndo.</p>

<p>Agora vá até <a href="<?php echo admin_url('admin.php?page=campaign_mobilize'); ?>">Mobilização</a> e defina todos os detalhes da página de mobilização da sua campanha:</p>
<ol>
    <li><i>Banners:</i> carregue aqui um banner quadrado de no mínimo 250x250 pixels que o sistema vai preparar automaticamente 3 tamanhos que seus apoiadores poderão colocar em sites, blogs ou mesmo nas redes sociais.</li>
    <li><i>Adesive sua foto:</i> carregue uma imagem pequena com seu nome e número, com isto seus apoiadores poderão adesivar suas próprias fotos para colocar nas redes sociais.</li>
    <li><i>Redes sociais:</i> insira o link das suas redes sociais para divulgar o seu perfil.</li>
    <li><i>Enviar para um amigo:</i> esta é uma mensagem padrão que seus apoiadores poderão enviar para várias pessoas. Insira o Assunto da Mensagem (ex: Eu apoio “Candidato X”) e um texto para a mensagem. Seja sucinto mas passe sua mensagem.</li>
</ol>
